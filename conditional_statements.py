temperature = 15

# If Statement for one condition:
if temperature > 30:
    # if Statements have to show indentations (space in front line) to be recognised
    print("It's warm")
    print("Drink water")
# elif (else if) statement for second conditions, has to be added right under if
elif temperature > 20:
    print("It's nice")
else:
    print("It's cold")
print("Done")
