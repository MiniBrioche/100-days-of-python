course = "Python Programming"
print(course.upper())  # Caps
print(course.lower())  # Lowercase
print(course.title())  # Uppercase evry first letter
print(course.strip())  # Takes away spaces, lstrip or rstrip possible
print(course.find("pro")) #Finds index (number of spots) of string
print(course.replace("p","j")) #replaces all p with j
print("pro" in course) #Boolean that checks if string contains "pro"
print("pro" not in course) #Boolean that checks if string doesn't contain "pro"